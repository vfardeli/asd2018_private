package org.alignprivate.asd.model;

public class EntryTerms {
	private int entryTermId;
	private Students student;
	private Terms terms;

	public int getEntryTermId() {
		return entryTermId;
	}

	public void setEntryTermId(int entryTermId) {
		this.entryTermId = entryTermId;
	}

	public Students getStudent() {
		return student;
	}

	public void setStudent(Students student) {
		this.student = student;
	}

	public Terms getTerms() {
		return terms;
	}

	public void setTerms(Terms terms) {
		this.terms = terms;
	}

}
